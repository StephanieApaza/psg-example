# Escribe un programa que reciba tres enteros por teclado y reste su resultado

num1 = int(input("Escribe un número entero: "))
num2 = int(input("Escribe un segundo número entero: "))
num3 = int(input("Escribe un tercer número entero: "))

resta = num1 - num2 - num3
print("La resta de los tres números enteros es: ", resta) 