# De las tuplas de coordenadas (x1,y1) = (-20,-20) y (x2,y2) = (40,20) encuentra las coordenadas del punto medio, almacena en una
# tupla e imprime el resultado (x,y)

# Fórmula del punto medio: ((x1 + x2)/2, (y1 + y2)/2)

# Coordenadas
coordenada_1 = (-20, -20)
coordenada_2 = (40, 20)


# Aplicación de la fórmula
x = (coordenada_1[0] + coordenada_2[0])/2
y = (coordenada_1[1]+ coordenada_2[1])/2

# Punto medio
punto_medio = (x, y)

print("El punto medio de las coordenadas es: ",punto_medio)