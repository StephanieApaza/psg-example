# Conversión de temperaturas de grados Fahrenheit a Celsius

# Temperaturas en grados Fahrenheit
grado_1 = 25
grado_2 = 300
grado_3 = 76

# Conversión a Celsius
a = (grado_1 - 32) * 5/9
b = (grado_2 - 32) * 5/9
c = (grado_3 - 32) * 5/9

# Resultados
print(grado_1, "°F es igual a =", round(a,2), "°C")
print(grado_2, "°F es igual a =", round(b,2), "°C")
print(grado_3, "°F es igual a =", round(c,2), "°C")
