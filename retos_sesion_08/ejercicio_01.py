# Ingresa por teclado tres coordenadas 'x', 'y' 'z', convierte los valores a enteros que almacenan los tres valores un una tupla

print("Este programa almacena tres coordenadas en una tupla")

x = int(input("Escribe un número entero para el valor de x: "))
y = int(input("Escribe un número entero para el valor de y: "))
z = int(input("Escribe un número entero para el valor de z: "))

coordenadas = (x, y, z)
print("Las coordenadas escritas son: ", coordenadas)